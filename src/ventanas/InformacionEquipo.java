    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed

        int validacion = 0;
        String tipo_equipo, marca, modelo, num_serie, status, observaciones;

        tipo_equipo = this.cmb_tipoEquipo.getSelectedItem().toString();
        marca = this.cmb_marcas.getSelectedItem().toString();
        status = this.cmb_estatus.getSelectedItem().toString();

        modelo = txt_modelo.getText().trim();
        num_serie = txt_numSerie.getText().trim();

        observaciones = txp_observaciones.getText();

        if (modelo.equals("")) {
            txt_modelo.setBackground(Color.red);
            validacion++;
        }
        if (num_serie.equals("")) {
            txt_numSerie.setBackground(Color.red);
            validacion++;
        }
        if (observaciones.equals("")) {
            txp_observaciones.setText("Sin Observaciones.");
        }

        //Si no hay campos vacios
        if (validacion == 0) {
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement(
                        "update equipos set tipo_equipo=?, marca=?, modelo=?, num_serie=?, observaciones=?, estatus=?, ultima_modificacion=? "
                        + "where id_equipo='" + IDequipo + "'");

                pst.setString(1, tipo_equipo);
                pst.setString(2, marca);
                pst.setString(3, modelo);
                pst.setString(4, num_serie);
                pst.setString(5, observaciones);
                pst.setString(6, status);
                pst.setString(7, user);
                pst.executeUpdate();
                cn.close();

                Limpiar();

                this.txt_nombreCliente.setBackground(Color.green);
                this.txt_fecha.setBackground(Color.green);
                this.txt_modelo.setBackground(Color.green);
                this.txt_numSerie.setBackground(Color.green);
                this.txt_ultimaModificacion.setText(user);

                JOptionPane.showMessageDialog(null, "Actualización Correcta.");
                this.dispose();

            } catch (SQLException e) {
                System.err.println("Error al actualizar los campos" + e);
                JOptionPane.showMessageDialog(null, "Error al actualizar Equipo, contacta con el Administrador");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos.");
        }

    }//GEN-LAST:event_btn_actualizarActionPerformed

