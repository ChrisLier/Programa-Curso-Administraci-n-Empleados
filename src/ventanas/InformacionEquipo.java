    public InformacionEquipo() {
        initComponents();
        user = Login.user;
        IDequipo = Informacion_Cliente.IDequipo;
        IDCliente_update = GestionarClientes.IDCliente_update;

        //Recuperar nombre del Equipo
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select nombre_cliente from clientes where id_cliente = '" + IDCliente_update + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                nom_cliente = rs.getString("nombre_cliente");
            }
        } catch (SQLException e) {
            System.err.println("Error al consultar el nombre del cliente" + e);
        }

        //Conultar la info del equipo y ponerla en su lugar
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select * from equipos where id_equipo = '" + IDequipo + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {

                this.cmb_tipoEquipo.setSelectedItem(rs.getString("tipo_equipo"));
                this.cmb_marcas.setSelectedItem(rs.getString("marca"));
                this.cmb_estatus.setSelectedItem(rs.getString("estatus"));
                this.txt_modelo.setText(rs.getString("modelo"));
                this.txt_numSerie.setText(rs.getString("num_serie"));
                this.txt_ultimaModificacion.setText(rs.getString("ultima_modificacion"));

                String dia = "", mes = "", annio = "";
                dia = rs.getString("dia_ingreso");
                mes = rs.getString("mes_ingreso");
                annio = rs.getString("annio_ingreso");
                txt_fecha.setText(dia + " del " + mes + " del " + annio);

                txp_observaciones.setText(rs.getString("observaciones"));
                txp_comentariosTecnico.setText(rs.getString("comentarios_tecnico"));
                lbl_revisionTecnicaDe.setText("Comentarios y actualización del Técnico: " + rs.getString("revision_tecnica_de"));

            }
        } catch (SQLException e) {
            System.err.println("Error al consultar la información del equipo" + e);
        }

        setTitle("Equipo del cliente " + nom_cliente);
        setSize(670, 530);
        setResizable(false);
        setLocationRelativeTo(null);

        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE); //pARA CERRAR solo esta ventana

        /// IMAGEN DE FONDO
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");   //Poner imagen de fondo
        //Adaptar el tamaño del JLabel LA IMAGEN Y ASÍ, con anchura, altura y que se escalable
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(lbl_wallpaper.getWidth(), lbl_wallpaper.getHeight(), Image.SCALE_DEFAULT));
        lbl_wallpaper.setIcon(icono);   //Colocamos el objeto en el label
        this.repaint(); //Necesario por que a veces no se ve la imagen.

        txt_nombreCliente.setText(nom_cliente);
    }

